public class OhYeah {
    String cok, cak, cik;

    public OhYeah(String cok, String cak, String cik) {
        this.cok = cok;
        this.cak = cak;
        this.cik = cik;
    }

    public String getCok() {
        return cok;
    }

    public void setCok(String cok) {
        this.cok = cok;
    }

    public String getCak() {
        return cak;
    }

    public void setCak(String cak) {
        this.cak = cak;
    }

    public String getCik() {
        return cik;
    }

    public void setCik(String cik) {
        this.cik = cik;
    }
}
